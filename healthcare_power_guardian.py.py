import sqlite3
import pandas as pd
from datetime import datetime

def init_hospital_power_system():
    conn = sqlite3.connect('hospital_power_hub.db')
    cursor = conn.cursor()

    # 1. Create Zone Table (Hospital Layout)
    cursor.execute('''
    CREATE TABLE IF NOT EXISTS Hospital_Zones (
        ZoneID INTEGER PRIMARY KEY,
        ZoneName TEXT,
        RequiredVoltage REAL, -- Usually 230V
        Criticality TEXT       -- Life-Support, Imaging, or General
    )
    ''')

    # 2. Create Sensor Log (The Heartbeat)
    cursor.execute('''
    CREATE TABLE IF NOT EXISTS Power_Sensors (
        ReadingID INTEGER PRIMARY KEY,
        Timestamp TEXT,
        ZoneID INTEGER,
        Voltage REAL,
        Temperature REAL,
        Status TEXT
    )
    ''')

    # 3. Populate Hospital Zones
    zones = [
        (1, 'ICU & Operating Theaters', 230.0, 'LIFE-SUPPORT'),
        (2, 'MRI & Radiology Wing', 230.0, 'DIAGNOSTIC'),
        (3, 'Admin & General Services', 230.0, 'NON-ESSENTIAL')
    ]
    cursor.executemany("INSERT OR REPLACE INTO Hospital_Zones VALUES (?,?,?,?)", zones)

    conn.commit()
    conn.close()
    print("🏥 St. Jude's Medical Power Hub: ARCHITECTURE INITIALIZED.")

init_hospital_power_system()

