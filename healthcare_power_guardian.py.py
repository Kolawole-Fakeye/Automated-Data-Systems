import plotly.graph_objects as go
from plotly.subplots import make_subplots
import sqlite3
import pandas as pd

def render_hospital_dashboard():
    conn = sqlite3.connect('hospital_power_hub.db')

    # 1. Fetch Source Data
    df_sources = pd.read_sql_query("SELECT SourceName, CurrentVoltage, Is_Active FROM Power_Sources", conn)
    # 2. Fetch Zone Data
    df_zones = pd.read_sql_query("SELECT ZoneName, Criticality FROM Hospital_Zones", conn)
    conn.close()

    # --- Create Dashboard ---
    fig = make_subplots(
        rows=1, cols=2,
        specs=[[{"type": "pie"}, {"type": "bar"}]],
        subplot_titles=("Current Power Mix (Source)", "Zone Criticality Distribution")
    )

    # Left Chart: Active Sources
    active_sources = df_sources[df_sources['Is_Active'] == 1]
    fig.add_trace(
        go.Pie(labels=active_sources['SourceName'],
               values=[1]*len(active_sources), # Equal weight for active sources in this view
               hole=.4,
               marker=dict(colors=['#00CC96', '#636EFA', '#EF553B'])),
        row=1, col=1
    )

    # Right Chart: Zone Overview
    fig.add_trace(
        go.Bar(x=df_zones['ZoneName'],
               y=[230, 230, 0], # Visualizing the "Shedding" of Admin (0V)
               name="Live Voltage (V)",
               marker_color=['#FF4B4B', '#FACC15', '#4B4B4B']),
        row=1, col=2
    )

    fig.update_layout(
        title_text="🏥 St. Jude's Medical Center: Live Infrastructure Health",
        template="plotly_dark",
        showlegend=True
    )

    fig.show()

render_hospital_dashboard()

