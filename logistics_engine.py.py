def update_shipment_status(shipment_id, new_lat, new_lon):
    conn = sqlite3.connect('logistics_tower.db')
    cursor = conn.cursor()

    # 1. Fetch the OLD position to compare
    cursor.execute("SELECT CurrentLat, CurrentLon FROM GlobalShipments WHERE ShipmentID = ?", (shipment_id,))
    old_data = cursor.fetchone()
    old_lat, old_lon = old_data[0], old_data[1]

    # 2. THE LOGIC: If position is EXACTLY the same, it's a potential delay
    if new_lat == old_lat and new_lon == old_lon:
        risk = "HIGH - Vessel Stationary"
        print(f"⚠️ ALERT: Shipment {shipment_id} has not moved. Risk set to HIGH.")
    else:
        risk = "Low"
        print(f"✅ Shipment {shipment_id} is on the move. Risk set to Low.")

    # 3. Update the database with the new info and the new Risk Level
    cursor.execute('''
        UPDATE GlobalShipments
        SET CurrentLat = ?, CurrentLon = ?, RiskLevel = ?
        WHERE ShipmentID = ?
    ''', (new_lat, new_lon, risk, shipment_id))

    conn.commit()
    conn.close()

