def fleet_audit():
    conn = sqlite3.connect('logistics_tower.db')
    # Use Pandas to read the whole fleet at once
    df = pd.read_sql_query("SELECT * FROM GlobalShipments", conn)

    def calculate_risk(row):
        # Professional Logic: Context-Aware Risk
        if row['LocalWeather'] == 'High Winds' and row['RiskLevel'] == 'High':
            return "MODERATE (Weather Delay)"
        elif row['LocalWeather'] == 'Clear' and row['RiskLevel'] == 'High':
            return "CRITICAL (Unknown Stasis)"
        return row['RiskLevel']

    df['RefinedRisk'] = df.apply(calculate_risk, axis=1)
    conn.close()

    return df[['ShipName', 'DestinationPort', 'LocalWeather', 'RefinedRisk']]

# Execute the Audit
fleet_summary = fleet_audit()
